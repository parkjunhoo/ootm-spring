package kr.team3.ootm.dao.review;

public class ReviewDTO {
	private int review_id;
	private int product_id;
	private String review_author_id;
	private String review_content;
	private int review_star;
	
	public int getReview_id() {
		return review_id;
	}
	public void setReview_id(int review_id) {
		this.review_id = review_id;
	}
	public int getProduct_id() {
		return product_id;
	}
	public void setProduct_id(int product_id) {
		this.product_id = product_id;
	}
	public String getReview_author_id() {
		return review_author_id;
	}
	public void setReview_author_id(String review_author_id) {
		this.review_author_id = review_author_id;
	}
	public String getReview_content() {
		return review_content;
	}
	public void setReview_content(String review_content) {
		this.review_content = review_content;
	}
	public int getReview_star() {
		return review_star;
	}
	public void setReview_star(int review_star) {
		this.review_star = review_star;
	}
	public ReviewDTO(int review_id, int product_id, String review_author_id, String review_content, int review_star) {
		super();
		this.review_id = review_id;
		this.product_id = product_id;
		this.review_author_id = review_author_id;
		this.review_content = review_content;
		this.review_star = review_star;
	}
	public ReviewDTO() {
		
	}
	@Override
	public String toString() {
		return "ReviewDTO [review_id=" + review_id + ", product_id=" + product_id + ", review_author_id="
				+ review_author_id + ", review_content=" + review_content + ", review_star=" + review_star + "]";
	}
	
}
